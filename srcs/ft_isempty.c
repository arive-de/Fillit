#include "fillit.h"

int		ft_isempty(char **map)
{
	int i;
	int j;

	i = 0;
	while (map[i])
	{
		j = 0;
		while (map[i][j])
		{
			if (map[i][j++] != '.')
				return (0);
		}
		i++;
	}
	return (1);
}
