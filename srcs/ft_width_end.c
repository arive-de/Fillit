#include "fillit.h"

int		ft_width_end(char **piece)
{
	int i;
	int j;

	i = 0;
	j = 3;
	while (piece[i][j] != '#')
	{
		i++;
		if (i == 4)
		{
			j--;
			i = 0;
		}
	}
	return (j);
}
